console.log(3 << 1); // 6 = 3 * 2^1
console.log(3 << 2); // 12 = 3 * 2^2
console.log(3 << 3); // 24 = 3 * 2^3
console.log(3 << 4); // 48 = 3 * 2^4


console.log(-3 << 1); // -6 = -3 * 2^1
console.log(-3 << 2); // -12 = -3 * 2^2
console.log(-3 << 3); // -24 = -3 * 2^3
console.log(-3 << 4); // -48 = -3 * 2^4